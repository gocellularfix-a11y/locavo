function interval(open: string, close: string): TimeInterval {
  return { open, close };
}

/** Mismo horario todos los días. */
function daily(open: string, close: string): OpeningHours {
  const day: DayHours = [interval(open, close)];
  return { weekly: [day, day, day, day, day, day, day] };
}

/** Horario semanal explícito, índice 0 = domingo. */
function weekly(...days: DayHours[]): OpeningHours {
  if (days.length !== 7) {
    throw new Error('weekly() requiere exactamente 7 días');
  }
  return { weekly: days };
}

